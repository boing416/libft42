#include "libft.h"

void	*ft_memccpy(void *s1, const void *s2, int c, size_t n)
{
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	while (n > 0)
	{
		if ((*str1++ = *str2++) == (unsigned char)c)
			return (str1);
		n--;
	}
	return (NULL);
}
